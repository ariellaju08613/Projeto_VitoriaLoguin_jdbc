package com.eleodoro.login.dao;

public class LoginPOJO {
    
    private String nome;
    private String senha;
    
    public LoginPOJO() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

}